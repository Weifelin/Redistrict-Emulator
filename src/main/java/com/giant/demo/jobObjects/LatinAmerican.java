package com.giant.demo.jobObjects;

public class LatinAmerican {
    private int min;
    private int high;

    public LatinAmerican() {
    }

    public LatinAmerican(int min, int high) {
        this.min = min;
        this.high = high;
    }

    public int getMin() {
        return min;
    }

    public void setMin(int min) {
        this.min = min;
    }

    public int getHigh() {
        return high;
    }

    public void setHigh(int high) {
        this.high = high;
    }
}
